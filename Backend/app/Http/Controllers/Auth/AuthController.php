    // Register
    public function handleRegister(Request $request)
    {
        // Validate d·ªØ li·ªáu ƒë·∫ßu v√†o
        $validated = Validator::make($request->all(), [
            'full_name' => 'required|string|max:255',
            'user_name' => 'required|string|max:255',
            'email' => 'required|string|email|max:255|unique:users',
            'password' => 'required|string|min:8|confirmed',
        ]);

        if ($validated->fails()) {
            return response()->json(['errors' => $validated->errors()], 400);
        }

        // T·∫°o ng∆∞·ªùi d√πng m·ªõi
        $user = User::create([
            'full_name' => $request->full_name,
            'user_name' => $request->user_name,
            'email' => $request->email,
            'password' => Hash::make($request->password),
        ]);

        // T·∫°o token cho ng∆∞·ªùi d√πng m·ªõi
        $token = $user->createToken('auth_token')->plainTextToken;

        return response()->json([
            'token' => $token,
            'user' => $user,
        ]);
    }

    public function sendResetPassword(Request $request)
    {
        $request->validate([
            'email' => 'required|email|exists:users,email',
        ]);

        $user = User::where('email', $request->email)->first();

        if (!$user) {
            return response()->json(['message' => 'Email kh√¥ng t·ªìn t·∫°i!'], 404);
        }

        // T·∫°o m·∫≠t kh·∫©u m·ªõi ng·∫´u nhi√™n
        $newPassword = Str::random(10);
        $user->password = Hash::make($newPassword);
        $user->save();

        // G·ª≠i email m·∫≠t kh·∫©u m·ªõi
        Mail::raw("M·∫≠t kh·∫©u m·ªõi c·ªßa b·∫°n l√†: $newPassword", function ($message) use ($user) {
            $message->to($user->email)
                ->subject('M·∫≠t kh·∫©u m·ªõi c·ªßa b·∫°n');
        });

        return response()->json(['message' => 'M·∫≠t kh·∫©u m·ªõi ƒë√£ ƒë∆∞·ª£c g·ª≠i qua email!']);
    }

